//---- Поиск в строке заданного фрагмента
 char *find (char *p,char *q){
	  int i;
	  for (; *p!='\0'; p++){ // Попарное сравнение до обнаружения расхождения
	  for (i=0 ; q[i]!='\0' && q[i]==p[i]; i++);
	  if ( q[i] == '\0') return p;          // Конец подстроки - успех
	  }                                      // иначе продолжить поиск
 return NULL;}
