#define SZ 1000
int A[SZ], sp=-1,lst=0,fst=0;
//------------------------------------------------- 1
void F1(){
	int c; if (sp < 1) return;
	c = A[sp]; A[sp]=A[sp-1]; A[sp-1]=c; }
//------------------------------------------------- 2
void F2(){
	int c; if (sp < 1) return;
	A[sp-1]+=A[sp]; sp--; }
//------------------------------------------------- 3
int F3(int n){
	int v,i;
	if (sp < n) return 0;
	v = A[sp-n];
	for (i=sp-n; i<sp; i++) A[i]=A[i+1];
	sp--; return v;}
//------------------------------------------------- 4
void F4(){ A[sp+1]=A[sp]; sp++; }
//------------------------------------------------- 5
int F5(int n){
	int v,i1,i2;
	i1 = (fst+n)%SZ;
	v = A[i1];
    for (; i1!=lst; i1=i2){
		i2 = (i1+1) % SZ;
		A[i1]=A[i2]; }
	lst = (lst==0 ? SZ-1 : lst-1); 
	return v;}
//------------------------------------------------- 6
void F6(){
	int n;
	if (fst==lst) return;
	n = (lst==0 ? SZ-1 : lst-1);
	A[lst++]=A[n];
	if (lst==SZ) lst = 0;}
 