//----- Сумма произвольного количества параметров по счетчику
 int sum(int n,...)		// n - счетчик параметров
 { int s,*p = &n+1;     // Указатель на область параметров
 for (s=0; n > 0; n--)	// назначается на область памяти вслед за счетчиком
 s += *p++;
 return(s); }
